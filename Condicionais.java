public class Condicionais {

    public static void main(String[] args) {
        
        int idade = 15;
        boolean amigoDoDono = true;

         if (idade > 18 && !amigoDoDono) {
             System.out.println("Pode Entrar");
         } else {
            System.out.println("Pode Entrar");
         }
    }
}